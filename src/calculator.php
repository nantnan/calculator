<?php 
    namespace  nantnan\calculator;
    class Calculator
    {
        public function add($f_num, $sec_num)
        {
            return $f_num + $sec_num;
        } 
         public function subtrac($f_num, $sec_num)
         {
            return $f_num - $sec_num;
        } 
        public function multiply($f_num, $sec_num)
        {
            return $f_num * $sec_num;
        } 
        public function divide($f_num, $sec_num)
        {
            return $f_num / $sec_num;
        } 
       




    }